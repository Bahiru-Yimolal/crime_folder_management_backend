// src/utils/scheduler.js
const cron = require("node-cron");
const {deleteExpiredUpcomingEvents} = require("../services/eventService");

// Runs every day at 1:00 AM (change time as needed)
cron.schedule("0 1 * * *", async () => {
  console.log(
    "[Event Cleanup] Running scheduled job to delete expired events..."
  );
  await deleteExpiredUpcomingEvents();
});
